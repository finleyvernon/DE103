ELSE PRINT 'There are 5 or less Touring-3000 bicycles.' ;  
GO

// Set the @Number variable to 5, 50, and 500 to test each statement.
DECLARE @Number int;  
SET @Number = 50;  
IF @Number > 100  
   PRINT 'The number is large.';  
ELSE   
   BEGIN  
      IF @Number < 10  
      PRINT 'The number is small.';  
   ELSE  
      PRINT 'The number is medium.';  
   END ;  
GO